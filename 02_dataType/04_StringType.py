#파이썬에서 문자열을 만드는 방법은 총 4가지로
# ', " , ''', """ 로 감싸면 문자열이 된다
print('Hello World')
print("Hello World")
print('''Hello World''')

print('''Hello
        World''')

print("""
        Hello
        World
        !
        """)

# '''와 """ 안에는 '와 " 둘 다 넣을 수 있다
print('''
올해 2023's의 목표
    1. 건강
    2. 취업
    ''')